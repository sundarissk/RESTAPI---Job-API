package com.jobs.QA.Util;

public class UserSkillPojo {
	// private variables or data members of POJO class
	private String user_id;
	private String skill_id;
	private String months_of_exp;
	
	// Getter and setter methods
	public String getUser_id() {
		return user_id;
	}
	public String getSkill_id() {
		return skill_id;
	}
	public String getMonths_of_exp() {
		return months_of_exp;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public void setSkill_id(String string) {
		this.skill_id = string;
	}
	public void setMonths_of_exp(String months_of_exp) {
		this.months_of_exp = months_of_exp;
	}
	
	
	
	

}
